N = int(input())
for _ in range(N):
    bucketA = [0] * 5
    bucketB = [0] * 5
    A = list(map(int,input().split()))
    B = list(map(int,input().split()))
for i in range(len(A)):
    bucketA[A[i]] += 1
for j in range(len(B)):
    bucketB[B[j]] += 1
for m in range(4,-1,-1):
    if bucketA[m] > bucketB[m]:
        print('A')
    elif bucketA[m] < bucketB[m]:
        print('B')
    else:
        print('D')























